def prob_res(i, j, a1, a2, d1, d2):
    l1 = a1/d2
    l2 = a2/d1
    return(math.exp(-l1)*math.exp(-l2)*math.pow(l1, i)*math.pow(l2, j)/math.factorial(i)/math.factorial(j))

def Epts(gp1, gp2, a1, a2, d1, d2):
    E = 0
    for i in range(5):
        for j in range(5):
            E += npts(gp1, gp2, i, j) * prob_res(i, j, a1, a2, d1, d2)
    return(E)

def melhor_resultado(a1, a2, d1, d2):
    Emax = 0
    imax = 0
    jmax = 0
    for i in range(5):
        for j in range(5):
            E = Epts(i, j, a1, a2, d1, d2)
            if E > Emax:
                Emax = E
                imax = i
                jmax = j
    return([imax, jmax])

