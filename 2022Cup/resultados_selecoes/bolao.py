def resultado(g1, g2):
    if g1>g2:
        res = "1"
    elif g2>g1:
        res = "2"
    else:
        res = "E"
    return(res)

def npts_tecgraf(gp1, gp2, gr1, gr2):
    rp = resultado(gp1, gp2)
    rr = resultado(gr1, gr2)
    if (rp == rr):
        npts = 5
        if gp1 > gp2:
            if (gp1 == gr1):
                npts += 3
            if (gp2 == gr2):
                npts += 2
        elif gp1 < gp2:
            if (gp1 == gr1):
                npts += 2
            if (gp2 == gr2):
                npts += 3 
        else:
            if (gp1 == gr1):
                npts += 5
    elif (gp1 == gr1) or (gp2 == gr2):
        npts = 1
    else:
        npts = 0
    return(npts)

