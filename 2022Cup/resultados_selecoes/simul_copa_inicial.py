def mata_mata(ajogar,alfa,beta):
    vencedor=[]
    ajogar_novo=[]
    for jogo in ajogar:
        t1 = jogo[0]-1
        t2 = jogo[1]-1
        g1 = gen_gols_Jogo(alfa[t1], beta[t2])
        g2 = gen_gols_Jogo(alfa[t2], beta[t1])
        if(g1==g2):
            gp1 = gen_gols_pror(alfa[t1], beta[t2])
            gp2 = gen_gols_pror(alfa[t2], beta[t1])
            if (gp1==gp2):
                vencedor.append(penaltis(t1+1,t2+1))
            elif(gp1>gp2):
                vencedor.append(t1+1)
            else:
                vencedor.append(t2+1)
        elif(g1>g2):
            vencedor.append(t1+1)
        else:
            vencedor.append(t2+1)
            
    vencedor.reverse()
    if(len(ajogar)>1):
        for i in range(int(len(ajogar)/2)):
            ajogar_novo.append([vencedor.pop(),vencedor.pop()])
    else:
        ajogar_novo=vencedor
    return(ajogar_novo)

