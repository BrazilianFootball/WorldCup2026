def gen_gols_Jogo(alfa, beta):
    lamb = alfa/beta
    g = 0
    x = -math.log(1- random.uniform(0, 1))
    while( x < lamb):
      x = x -math.log(1 - random.uniform(0, 1))
      g = g+1
    return(g)

#Gols marcados durante uma prorrogacao, poisson((alfa/beta)/3) 
def gen_gols_pror(alfa, beta):
    lamb = (alfa/beta)/3.
    g = 0
    x = -math.log(1- random.uniform(0, 1))
    while( x < lamb):
      x = x -math.log(1 - random.uniform(0, 1))
      g = g+1
    return(g)

def penaltis(time1, time2):
    if (random.uniform(0,1)>=0.5):
        return(time1)
    else:
        return(time2)

