def gerar_class (grupo, alfa, beta):
    
    #print (grupo)
    sel_ids= [0 for i in range(32)]
    cod=0
    for n in grupo:
        sel_ids[n-1]=cod
        cod+=1
    ajogar=[0 for j in range(6)]
    ajogar[0]=[grupo[0],grupo[1]]
    ajogar[1]=[grupo[0],grupo[2]]
    ajogar[2]=[grupo[0],grupo[3]]
    ajogar[3]=[grupo[1],grupo[2]]
    ajogar[4]=[grupo[1],grupo[3]]
    ajogar[5]=[grupo[2],grupo[3]]
    
    cl = [[0 for i in range(5)] for j in range(4)]
    for i in range(len(grupo)):
       cl[i][0] = grupo[i]       
    for i in range(6):
       t1 = sel_ids[ajogar[i][0]-1]
       t2 = sel_ids[ajogar[i][1]-1]
       g1 = gen_gols_Jogo(alfa[ajogar[i][0]-1], beta[ajogar[i][1]-1])
       g2 = gen_gols_Jogo(alfa[ajogar[i][1]-1], beta[ajogar[i][0]-1])
       cl[t1][3] += g1
       cl[t1][2] += (g1 - g2)
       cl[t2][3] += g2
       cl[t2][2] += (g2 - g1)
        #Caso os tres criterios deem empatados(pontos, saldo de gols e gols marcados)
       cl[t1][4]=random.uniform(0,1)
       cl[t2][4]=random.uniform(0,1)
       if (g1 > g2):
            cl[t1][1] += 3
       elif (g2 > g1):
            cl[t2][1] += 3
       else:
            cl[t1][1] += 1
            cl[t2][1] += 1
       #print([t1, g1, g2, t2])
        
    for col in [4, 3, 2, 1]:
        cl = sorted(cl, key = operator.itemgetter(col), reverse = True)
    #print(cl)
    return(cl)

