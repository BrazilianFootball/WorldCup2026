def Semifinais(ajogar,alfa,beta):
    ajogar_finais = mata_mata(ajogar,alfa,beta)
    if (ajogar_finais[0][0]==ajogar[0][0]):
        time_terc_1=ajogar[0][1]
    else:
        time_terc_1=ajogar[0][0]
    
    if (ajogar_finais[0][1]==ajogar[1][0]):
        time_terc_2=ajogar[1][1]
    else:
        time_terc_2=ajogar[1][0]
        
    ajogar_terc=[[time_terc_1,time_terc_2]]
    return([ajogar_finais,ajogar_terc])

